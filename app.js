function add(a, b) {
    return a + b
}
function sub(a, b) {
    return a - b
}
function mui(a, b) {
    return a * b
}
function div(a, b) {
    return a / b
}
function add2(a, b, c) {
    return a + b + c
}
function sub2(a, b, c) {
    return a - b - c
}
function mui2(a, b, c) {
    return a * b * c
}
function div2(a, b, c) {
    return a / b / c
}